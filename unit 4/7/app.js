function difference(x, y) {
  return x - y;
}

function product(x, y) {
  return x + y;
}

function printDay(x) {
  const days = {
    1: "Sunday",
    2: "Monday",
    3: "Tuesday",
    4: "Wednesday",
    5: "Thursday",
    6: "Friday",
    7: "Saturday",
  };
  return days[x];
}

function lastElement(arr) {
  return arr[arr.length - 1];
}
